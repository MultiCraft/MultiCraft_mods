	default.register_bedrock_v6()
elseif mg_name == "valleys" then
	default.register_bedrock_valleys()
end

if mg_name == "v6" then
	default.register_ores()
	default.register_mgv6_decorations()
else
	default.register_biomes()
	default.register_ores()
	default.register_decorations()
end
